    private void actualizarBarraProgreso() {
        int progreso = 0;
        if (!jTextField1.getText().isEmpty()) progreso += 20;
        if (!jTextField2.getText().isEmpty()) progreso += 20;
        if (!jTextField3.getText().isEmpty()) progreso += 20;
        if (!jTextField4.getText().isEmpty()) progreso += 20;
        if (!jTextField5.getText().isEmpty()) progreso += 20;
        jProgressBar1.setValue(progreso);
    
    }
    private void agregarDocumentListeners() {
    DocumentListener listener = new DocumentListener() {
        @Override
        public void insertUpdate(DocumentEvent e) {
            actualizarBarraProgreso();
        }

        @Override
        public void removeUpdate(DocumentEvent e) {
            actualizarBarraProgreso();
        }

        @Override
        public void changedUpdate(DocumentEvent e) {
            actualizarBarraProgreso();
        }
    };

    jTextField1.getDocument().addDocumentListener(listener);
    jTextField2.getDocument().addDocumentListener(listener);
    jTextField3.getDocument().addDocumentListener(listener);
    jTextField4.getDocument().addDocumentListener(listener);
    jTextField5.getDocument().addDocumentListener(listener);
}
    
    
