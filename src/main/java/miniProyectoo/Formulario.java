    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          int filaSeleccionada = jTable1.getSelectedRow();
        if (filaSeleccionada != -1) {
            modeloTabla.setValueAt(jTextField1.getText(), filaSeleccionada, 0);
            modeloTabla.setValueAt(jTextField2.getText(), filaSeleccionada, 1);
            modeloTabla.setValueAt(jTextField3.getText(), filaSeleccionada, 2);
            modeloTabla.setValueAt(jTextField4.getText(), filaSeleccionada, 3);
            modeloTabla.setValueAt(jTextField5.getText(), filaSeleccionada, 4);
            modeloTabla.setValueAt(jRadioButton1.getText(), filaSeleccionada, 5);
            limpiarCampos();
            actualizarBarraProgreso();  // Actualiza la barra de progreso
            JOptionPane.showMessageDialog(this, "Contacto actualizado exitosamente.");
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un contacto para editar.");
        }
        confirmarYEliminar();
    }//GEN-LAST:event_jButton2ActionPerformed

