    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
         String nombreBusqueda = jTextField6.getText().trim().toLowerCase(); // Nombre a buscar en minúsculas

    boolean encontrado = false;

    // Recorre la tabla para buscar el contacto por nombre
    for (int i = 0; i < modeloTabla.getRowCount(); i++) {
        String nombreEnTabla = modeloTabla.getValueAt(i, 0).toString().toLowerCase(); // Nombre en la tabla en minúsculas

        if (nombreEnTabla.equals(nombreBusqueda)) {
            // Selecciona la fila encontrada y enfoca la tabla
            jTable1.setRowSelectionInterval(i, i);
            jTable1.scrollRectToVisible(jTable1.getCellRect(i, 0, true));
            encontrado = true;
            break;
        }
    }

    if (!encontrado) {
        JOptionPane.showMessageDialog(this, "Contacto no encontrado.", "Resultado de búsqueda", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_jButton5ActionPerformed

