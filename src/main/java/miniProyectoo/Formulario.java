    private void agregarContacto() {
        String nombre = jTextField1.getText().trim();
        String apellido = jTextField2.getText().trim();
        String telefono = jTextField3.getText().trim();
        String correo = jTextField4.getText().trim();
        String direccion = jTextField5.getText().trim();
        String estadoCivil = obtenerEstadoCivilSeleccionado();
        
        System.out.println("Nombre: " + nombre + ", Apellido: " + apellido + ", Telefono: " + telefono + ", Correo: " + correo + ", Direccion: " + direccion + ", Estado Civil: " + estadoCivil);
        
        if (nombre.isEmpty() || apellido.isEmpty() || !validarTelefono(telefono) || !validarCorreo(correo) || direccion.isEmpty() || estadoCivil.isEmpty() ) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese datos válidos.");
        return;
    }

    // Verificar duplicados
    for (int i = 0; i < modeloTabla.getRowCount(); i++) {
        if (modeloTabla.getValueAt(i, 0).equals(nombre) || modeloTabla.getValueAt(i, 2).equals(telefono)) {
            JOptionPane.showMessageDialog(this, "El contacto ya existe.");
            return;
        }
    }

    modeloTabla.addRow(new Object[]{nombre, apellido, telefono, correo, direccion, estadoCivil});
    limpiarCampos();
    actualizarBarraProgreso();
    JOptionPane.showMessageDialog(this, "Contacto agregado exitosamente.");
}
    private String obtenerEstadoCivilSeleccionado() {
   String estadoCivil = "";
    if (jRadioButton1.isSelected()) {
        estadoCivil = jRadioButton1.getText(); // Soltero
    } else if (jRadioButton2.isSelected()) {
        estadoCivil = jRadioButton2.getText(); // Casado
    } else if (jRadioButton3.isSelected()) {
        estadoCivil = jRadioButton3.getText(); // Divorciado
    } else if (jRadioButton5.isSelected()) {
        estadoCivil = jRadioButton5.getText(); // Viudo
    }
    System.out.println("Estado Civil Seleccionado: " + estadoCivil); // Para depuración
    return estadoCivil;
}
    
    private boolean validarTelefono(String telefono) {
    return telefono.matches("\\d{10}");  // Acepta solo 10 dígitos
}

// Validación de correo electrónico
private boolean validarCorreo(String correo) {
    return correo.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$") && correo.endsWith(".com");
}
    

